package by.komposter.ObjectFactory;

public class Node {
    int idNode,idNodeUser,idHost,idSbe,idDepartment;
    String nodeName, location;

    public int getIdNode() {
        return idNode;
    }

    public void setIdNode(int idNode) {
        this.idNode = idNode;
    }

    public int getIdNodeUser() {
        return idNodeUser;
    }

    public void setIdNodeUser(int idNodeUser) {
        this.idNodeUser = idNodeUser;
    }

    public int getIdHost() {
        return idHost;
    }

    public void setIdHost(int idHost) {
        this.idHost = idHost;
    }

    public int getIdSbe() {
        return idSbe;
    }

    public void setIdSbe(int idSbe) {
        this.idSbe = idSbe;
    }

    public int getIdDepartment() {
        return idDepartment;
    }

    public void setIdDepartment(int idDepartment) {
        this.idDepartment = idDepartment;
    }

    public String getNodeName() {
        return nodeName;
    }

    public void setNodeName(String nodeName) {
        this.nodeName = nodeName;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

}
